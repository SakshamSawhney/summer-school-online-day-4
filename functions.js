// Declaration
function add(a, b) {
  return a + b;
}

// Expression
const square = function(n) {
  return n * n;
};

// Arrow Function
const greet = (name) => `Hello, ${name}!`;

// Calling and Logging
console.log("Sum:", add(5, 7));
console.log("Square:", square(4));
console.log(greet("Saksham"));
